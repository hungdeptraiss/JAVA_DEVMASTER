public class Lab02_BT10 {
    public static void main(String[] args) {
        char color = 'R';

        if (color == 'R' || color == 'r') {
            System.out.println("RED");
        } else if (color == 'G' || color == 'g') {
            System.out.println("GREEN");
        } else if (color == 'B' || color == 'b') {
            System.out.println("BLUE");
        } else {
            System.out.println("BLACK");
        }
    }
}
